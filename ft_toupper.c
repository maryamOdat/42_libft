#include "libft.h"

int	ft_toupper(int ch)
{
	if (ch >= 97 && ch <= 122)
	{
		return (ch - (97 - 65));
		ch = ch - 32;
		return (ch);
	}
	else
	{
		return (ch);
	}
}
