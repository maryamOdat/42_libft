#include "libft.h"

char	*ft_strrchr(char const *s, int c)
{
	char	*b;
	size_t	i;

	i = 0;
	b = NULL;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
		{
			b = (char *)&s[i];
		}
		i++;
	}
	if ((char) c == '\0')
	{
		return ((char *)&s[i]);
	}
	return (b);
}
